/// Common ASCII codes
class AsciiRunes {
  /// tab
  static const int runeTab = 9;

  /// LF, \n on Unix systems
  static const int runeLineFeed = 10;

  /// CR, together with LF - so CRLF - a line break in IMAP, POP3 and SMTP
  static const int runeCarriageReturn = 13;

  /// space
  static const int runeSpace = 32;

  /// "double quote"
  static const int runeDoubleQuote = 34;

  /// &
  static const int runeAmpersand = 38;

  /// 'single quote'
  static const int runeSingleQuote = 39;

  /// (
  static const int runeOpeningParentheses = 40;

  /// )
  static const int runeClosingParentheses = 41;

  /// ,
  static const int runeComma = 44;

  /// -
  static const int runeMinus = 45;

  /// .
  static const int runeDot = 46;

  /// /
  static const int runeSlash = 47;

  /// 0
  static const int rune0 = 48;

  /// 9
  static const int rune9 = 57;

  /// ;
  static const int runeSemicolon = 59;

  /// <
  static const int runeSmallerThan = 60;

  /// =
  static const int runeEquals = 61;

  /// >
  static const int runeGreaterThan = 62;

  /// @
  static const int runeAt = 64;

  /// A
  static const int runeAUpperCase = 65;

  /// Z
  static const int runeZUpperCase = 90;

  /// [
  static const int runeOpeningBracket = 91;

  /// \
  static const int runeBackslash = 92;

  /// ]
  static const int runeClosingBracket = 93;

  /// _
  static const int runeUnderline = 95;

  /// a
  static const int runeALowerCase = 97;

  /// z
  static const int runeZLowerCase = 122;
}
